addEvent(document.getElementById("blog_others"), "change", function(event) {
  if (document.getElementById("blog_others").checked) {
    document.getElementById("blog_html").checked=false;
    document.getElementById("blog_css").checked=false;
    document.getElementById("blog_javascript").checked=false;
    document.getElementById("blog_ror").checked=false;
    document.getElementById("blog_python").checked=false;
    document.getElementById("blog_java").checked=false;
    document.getElementById("blog_cpp").checked=false;
    document.getElementById("blog_sql").checked=false;
    document.getElementById("blog_git").checked=false;
    document.getElementById("blog_linux").checked=false;
  }
});


